#include <iostream>
using namespace std;

int main()
{
    ios::sync_with_stdio(NULL);
    cin.tie(NULL);
    cout.tie(NULL);
    int dp[10001];
    dp[1] = 1;
    dp[2] = 2;
    dp[3] = 3;
    int odd = 1;
    int even = 1;
    int n, a;
    cin >> n;

    for (int i = 4; i <= 10000; i++)
    {
        if (i % 3 == 0)
        {
            if (i % 2 == 0)
            {
                even++;
            }
            else
            {
                odd++;
            }
        }

        if (i % 2 == 0)
        {
            dp[i] = dp[i - 1] + even;
        }
        else
        {
            dp[i] = dp[i - 1] + odd;
        }
    }
    for (int i = 0; i < n; i++)
    {
        cin >> a;
        cout << dp[a] << "\n";
    }

    return 0;
}

